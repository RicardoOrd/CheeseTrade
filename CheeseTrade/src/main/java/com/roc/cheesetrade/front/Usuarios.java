private void cargarUsuarios() {
    panelUsuarios.removeAll();
    UsuarioDAO dao = new UsuarioDAO();
    List<Usuario> lista = dao.listar();

    for (Usuario u : lista) {
        javax.swing.JPanel tarjeta = new javax.swing.JPanel();
        tarjeta.setLayout(new java.awt.GridLayout(0, 1));
        tarjeta.setBorder(javax.swing.BorderFactory.createTitledBorder(u.getUsuario()));

        tarjeta.add(new javax.swing.JLabel("Correo: " + u.getCorreo()));
        tarjeta.add(new javax.swing.JLabel("Celular: " + u.getCelular()));
        tarjeta.add(new javax.swing.JLabel("Direcci√≥n: " + u.getDireccion()));
        tarjeta.add(new javax.swing.JLabel("Rol: " + u.getRol()));

        panelUsuarios.add(tarjeta);
    }

    panelUsuarios.revalidate();
    panelUsuarios.repaint();
}


